create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create table if not exists public.profiles (
  user_id uuid primary key references auth.users (id) on delete cascade,
  username text not null unique check (char_length(username) between 2 and 24),
  avatar_id text not null default 'rocket',
  total_xp integer not null default 0 check (total_xp >= 0),
  unlocked_badges jsonb not null default '[]'::jsonb,
  consistency_reward_milestones integer[] not null default '{}'::integer[],
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

drop trigger if exists profiles_set_updated_at on public.profiles;
create trigger profiles_set_updated_at
before update on public.profiles
for each row
execute function public.set_updated_at();

create table if not exists public.training_completions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles (user_id) on delete cascade,
  date_key date not null,
  task_id text not null,
  task_title text not null,
  xp integer not null check (xp >= 0),
  created_at timestamptz not null default timezone('utc', now()),
  unique (user_id, date_key, task_id)
);

create index if not exists training_completions_user_date_idx
  on public.training_completions (user_id, date_key desc);

create table if not exists public.challenge_completions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles (user_id) on delete cascade,
  challenge_id text not null,
  title text not null,
  xp integer not null check (xp >= 0),
  badge jsonb not null,
  completed_on date not null default current_date,
  created_at timestamptz not null default timezone('utc', now()),
  unique (user_id, challenge_id)
);

create index if not exists challenge_completions_user_date_idx
  on public.challenge_completions (user_id, completed_on desc);

create table if not exists public.coach_quotes (
  id bigint generated by default as identity primary key,
  quote text not null unique,
  created_by uuid references public.profiles (user_id) on delete set null,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.coach_challenges (
  id text primary key,
  title text not null,
  description text not null,
  target text not null,
  xp integer not null default 130 check (xp >= 0),
  level_required integer not null default 2 check (level_required >= 1),
